"""
Create a python Script to ask user to enter any two numbers (float / integer). 
Write a program to calculate the sum of these two numbers and print out the message “the sum of these two number is: [sum]”.
"""
first_no = int(input("Enter first number(integer): "))
second_no = float(input("Enter second number(float): "))
sum = first_no + second_no
print("the sum of these two number is: ",sum)
